package feb23;

public class ComparisonOperators
{
    public static void main(String[] args) {
        int x = 5;
        int y = 10;
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x == y);
        System.out.println(x != y);
    }
}
